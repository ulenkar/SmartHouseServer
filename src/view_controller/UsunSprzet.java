    private void usunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_usunActionPerformed
        int[] selected = lista.getSelectedIndices();
        for (int i : selected) {
            Sprzet s = (Sprzet) listModel.getElementAt(i);
            informacja.setText("Usunięto sprzęt: " + s.getOpis());
            menadzer.deleteSprzet(s);
        }
        menadzer.refreshSprzet();
        wyswietlSprzet();
    }//GEN-LAST:event_usunActionPerformed

