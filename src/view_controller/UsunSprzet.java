    private void wyswietlSprzet() {
        listModel = new DefaultListModel<>();
        ArrayList<Sprzet> sprzet = null;
        if ("czujnik".equals(rodzaj)) {
            sprzet = menadzer.getCzujniki();
        }
        if ("gniazdko".equals(rodzaj)) {
            sprzet = menadzer.getGniazdka();
        }
        if (!(sprzet == null) && !(sprzet.isEmpty())) {
            for (Sprzet s : sprzet) {
                //model.addElement(s.getSprzetId() + ") " + s.getOpis());
                listModel.addElement(s);
            }
        }
        lista.setModel(listModel);
    }

